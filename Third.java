public class Third {
    static void func() {
        System.out.println("First function");
    }

    static void func1() {
        System.out.println("second function");
    }

    static void func2() {
        System.out.println("third function");
    }

    public static void main(String[] args) {
        System.out.println("All functions");
        func();
        func1();
        func2();
    }
}
